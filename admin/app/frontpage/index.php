  <br /><br /><h1>Digital Signage Powered by Community</h1>
  <p>Concerto introduces an entirely new way of reaching out to the rest of RPI's campus.  With Concerto, you can communicate events, announcements, and other important information to campus through a variety of digital means.</p>
  <p>A growing number of high-resolution displays have been deployed in high-traffic areas of RPI's Troy campus.  These screens can pull from the collection of content categories, also known as <i>feeds</i>, already in the system and open to submissions from the community.</p>
  <p>Integration with existing services, such as dining halls and the Institute Events Calendar, is also dead simple - Concerto simply retrieves content from them.  Content on each screen can be controlled by the people who are most directly connected to that display.</p>
